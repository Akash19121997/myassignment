package assignment;

public class calculator {
public int add(int a,int b) {
	return a+b;
}
public int multiply(int a,int b) {
	return a*b;
}
public float divide(int a,int b) {
	return a/b;
}
public int sub(int a,int b) {
	return a-b;
}
public double sq(int a) {
	return Math.sqrt(a);
	
}
public double per(int a) {
	return (double)a/100;
}
}
